    <div class="section">
        <h2>Main Attractions</h2>
        <p>
            Jerash is internationally known for its extensive Greco-Roman ruins, including the Arch of Hadrian, the Oval Plaza, the colonnaded Cardo Maximus, the South and North Theatres, temples, baths, and city gates. 
            The Jerash Archaeological Park showcases one of the most complete provincial Roman cities ever discovered. 
            Every summer, the Jerash Festival of Culture and Arts brings music, theatre, and dance performances to the city’s ancient venues, celebrating its cultural heritage.
        </p>

