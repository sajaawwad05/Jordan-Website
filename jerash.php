        <h2>About Jerash</h2>
        <p>Jerash, located north of Amman, is one of the best-preserved Roman cities in the world and the capital of Jerash Governorate. 
        Known in antiquity as Gerasa, the city flourished during the Roman period as part of the Decapolis, leaving behind a remarkably intact urban layout. 
        Modern Jerash blends a growing contemporary city with its expansive archaeological park, making it a major cultural and historical destination in Jordan.</p>
